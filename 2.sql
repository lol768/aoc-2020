select x.password, x.letter, x.min, x.max
from (
         select array(select (regexp_matches(password, letter, 'g'))) as matches, *
         from day2) x
where array_length(x.matches, 1) >= min
  and array_length(x.matches, 1) <= max;

select x.password, x.letter, x.min, x.max
from (
         select substr(password, min, 1) as lower, substr(password, max, 1) as upper, *
         from day2) x
where (x.lower = x.letter or x.upper = x.letter)
  and (x.upper != x.lower);
